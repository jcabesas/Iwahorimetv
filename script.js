/* Normalize everything the SAME way */
function normalize(text) {
  return text
    .toUpperCase()
    .replace(/Ñ/g, "N")
    .replace(/\./g, "")        // remove dots
    .replace(/\s+/g, " ")      // fix double spaces
    .trim();
}

/* Pre-normalize list ONCE */
const metvSet = new Set(metvList.map(normalize));

function checkMETV() {
  const last = normalize(document.getElementById("lastname").value);
  const first = normalize(document.getElementById("firstname").value);
  const middle = normalize(document.getElementById("middlename").value);
  const result = document.getElementById("result");

  if (!last || !first || !middle) {
    result.innerHTML =
      "<span style='color:#d93025;'>⚠ Please complete all fields.</span>";
    return;
  }

  // Combine EXACTLY like the list
  const fullName = `${last}, ${first} ${middle}`;

  console.log("Checking:", fullName);

  if (metvSet.has(fullName)) {
    result.innerHTML =
      "<span style='color:#188038;'>🎉 Congratulations! You are <b>METV</b>.</span>";
  } else {
    result.innerHTML =
      "<span style='color:#d93025;'>❌ Sorry, you are <b>NOT METV</b>.</span>";
  }
}
